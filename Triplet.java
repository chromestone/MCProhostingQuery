public class Quad {
	private Object _one;
	private Object _two;
	private Object _three;
	private Object _four;
	
	public Quad(Object one, Object two, Object three, Object four) {
		_one = one;
		_two = two;
		_three = three;
		_four = four;
	}
	
	public Object getOne() {
		return _one;
	}
	
	public Object getTwo() {
		return _two;
	}
	
	public Object getThree() {
		return _three;
	}
	public Object getFour() {
		return _four;
	}
}
